package com.topaz.trading.model;

public class Funds {

	
	private String segmentAll;
	private String segmentCom;
	
	private String actid;
	private String uid;
	
	private String key;

	

	public String getSegmentAll() {
		return segmentAll;
	}

	public void setSegmentAll(String segmentAll) {
		this.segmentAll = segmentAll;
	}

	public String getSegmentCom() {
		return segmentCom;
	}

	public void setSegmentCom(String segmentCom) {
		this.segmentCom = segmentCom;
	}

	public String getActid() {
		return actid;
	}

	public void setActid(String actid) {
		this.actid = actid;
	}

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}
	
	
	
	
	
	
}
